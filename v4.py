def build_base_dataset(opsd_path, weather_path, output_file, sample_mode=False):
    print('[STEP 1] Validating files...')
    for path, label in [(opsd_path,'OPSD'),(weather_path,'Weather')]:
        if not validate_file_exists(path, label):
            return None, []

    nrows   = 10_000 if sample_mode else None
    df_opsd = load_csv_safely(opsd_path,    nrows=nrows)
    df_weat = load_csv_safely(weather_path, nrows=nrows)

    print('[STEP 2] Parsing timestamps...')
    for df in (df_opsd, df_weat):
        if 'utc_timestamp' in df.columns:
            df['utc_timestamp'] = pd.to_datetime(df['utc_timestamp'], utc=True, errors='coerce')

    print('[STEP 3] Mapping columns...')
    all_countries = sorted(set(get_country_codes(df_opsd.columns)) | set(get_country_codes(df_weat.columns)))
    mapping, country_features = build_column_mapping(df_opsd, df_weat, all_countries)
    countries = get_countries_with_wind(country_features, all_countries)
    print(f'  {len(countries)} countries with wind: {countries}')

    print('[STEP 4] Aligning on OPSD timestamp grid...')
    opsd_idx = df_opsd.set_index('utc_timestamp')
    weat_idx = df_weat.set_index('utc_timestamp').reindex(opsd_idx.index).fillna(0)

    print('[STEP 5] Assembling dataset...')
    df = pd.DataFrame(index=opsd_idx.index)
    if 'cet_cest_timestamp' in df_opsd.columns:
        df['cet_cest_timestamp'] = df_opsd['cet_cest_timestamp'].values
    for country in countries:
        df = extract_all_features(df, mapping, country, opsd_idx, weat_idx)
    df = df.reset_index().rename(columns={'index':'utc_timestamp'})
    print(f'  Assembled {len(df):,} rows x {len(df.columns)} cols')

    print('[STEP 6] Cleaning...')
    df = clean_and_optimize(df)

    print('[STEP 7] Applying physics features...')
    df = apply_physics_features(df, countries)

    print('[STEP 8] Organising columns...')
    df = organize_columns_by_country(df, countries)

    print('[STEP 9] Saving...')
    df.to_csv(output_file, index=False)
    mb = os.path.getsize(output_file) / 1_048_576
    print(f'  Saved: {output_file}  ({mb:.2f} MB)')

    print(f'\nDataset shape: {df.shape}')
    return df, countries

