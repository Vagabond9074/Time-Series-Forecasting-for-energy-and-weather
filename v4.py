def validate_file_exists(path, label=''):
    if not os.path.exists(path):
        print(f'  [ERROR] {label} not found: {path}')
        return False
    if os.path.getsize(path) == 0:
        print(f'  [ERROR] {label} is empty: {path}')
        return False
    return True


def load_csv_safely(path, nrows=None):
    print(f'  Loading: {os.path.basename(path)}')
    for i, kwargs in enumerate([
        dict(low_memory=False, nrows=nrows),
        dict(nrows=nrows),
        dict(engine='python', nrows=nrows),
        dict(encoding='latin1', nrows=nrows),
        dict(encoding='utf-8', nrows=nrows),
    ], 1):
        try:
            df = pd.read_csv(path, **kwargs)
            print(f'    OK (strategy {i}) — {len(df):,} rows, {len(df.columns)} cols')
            return df
        except Exception:
            continue
    raise ValueError(f'Could not load: {path}')


def get_country_codes(columns):
    codes = set()
    for col in columns:
        parts = col.split('_')
        if len(parts) >= 2 and len(parts[0]) == 2 and parts[0].isalpha() and parts[0].isupper():
            codes.add(parts[0])
    return sorted(codes)

"""## Column Mapping"""

def build_column_mapping(df_opsd, df_weather, all_countries):
    patterns = {
        'load':        ['_load_actual_entsoe_transparency', '_load'],
        'solar':       ['_solar_generation_actual', '_solar'],
        'wind_power':  ['_wind_generation_actual', '_wind_onshore_generation_actual', '_wind'],
        'temperature': ['_temperature'],
        'wind_speed':  ['_wind_speed', '_wind_velocity'],
    }
    mapping, country_features = {}, {}
    for country in all_countries:
        for feature, candidates in patterns.items():
            for suffix in candidates:
                col = f'{country}{suffix}'
                if col in df_opsd.columns or col in df_weather.columns:
                    mapping[col] = f'{country}_{feature}'
                    country_features.setdefault(country, set()).add(feature)
                    break
    return mapping, country_features


def get_countries_with_wind(country_features, all_countries):
    return [c for c in all_countries if 'wind_power' in country_features.get(c, set())]

"""## Feature Extraction"""

def extract_all_features(df, mapping, country, opsd_idx, weather_idx):
    for feature in ['load', 'temperature', 'solar', 'wind_power']:
        src_cols = [k for k, v in mapping.items() if v == f'{country}_{feature}']
        if not src_cols:
            continue
        src = src_cols[0]
        source = opsd_idx if src in opsd_idx.columns else weather_idx if src in weather_idx.columns else None
        if source is not None:
            df[f'{country}_{feature}'] = source[src].reindex(df.index).fillna(0).values

    ws_cols = [k for k, v in mapping.items() if v == f'{country}_wind_speed']
    if ws_cols and ws_cols[0] in weather_idx.columns:
        df[f'{country}_wind_speed_m_s']  = weather_idx[ws_cols[0]].reindex(df.index).fillna(0).values
        df[f'{country}_wind_speed_km_h'] = df[f'{country}_wind_speed_m_s'] * 3.6
    return df

"""## Physics-Based Features"""

def _ts(df, field):
    if 'utc_timestamp' in df.columns:
        return getattr(df['utc_timestamp'].dt, field)
    return pd.Series(getattr(df.index, field), index=df.index)


def apply_physics_features(df, countries):
    for country in countries:
        tc = f'{country}_temperature'
        if tc not in df.columns:
            continue
        temp = df[tc].ffill().bfill()

        df[f'{country}_hdd'] = np.maximum(0, 18 - temp)
        df[f'{country}_cdd'] = np.maximum(0, temp - 22)

        month = _ts(df, 'month')
        sf = np.where(month.isin([6,7,8]), 0.85, np.where(month.isin([12,1,2]), 0.70, 0.75))
        bd = np.where(temp > 20, temp*0.10, np.where(temp > 0, temp*0.15, temp*0.20))
        dp = temp - bd * sf
        df[f'{country}_dew_point'] = dp.clip(lower=temp-12, upper=temp-1)

        eff  = np.where(temp < -10, 0.85, np.where(temp < 0, 0.92, 0.98))
        hour = _ts(df, 'hour')
        tf   = np.where(hour.isin(set(range(0,6)) | set(range(18,24))), 1.25, 1.0)
        df[f'{country}_heating_intensity'] = df[f'{country}_hdd'] * eff * tf

        ce   = np.where(temp > 35, 0.80, np.where(temp > 28, 0.90, 0.95))
        biz  = np.where(hour.isin(range(9,18)), 1.35, np.where(hour.isin([7,8,18,19]), 1.15, 0.85))
        wknd = np.where(_ts(df, 'weekday') >= 5, 1.20, 1.0)
        df[f'{country}_cooling_intensity'] = df[f'{country}_cdd'] * ce * biz * wknd

        target = f'{country}_wind_speed_km_h'
        if target not in df.columns:
            for col in [f'{country}_wind', f'{country}_wind_power', f'{country}_wind_generation']:
                if col in df.columns:
                    df[target] = 10 * np.cbrt(df[col].fillna(0) / 1000 + 1)
                    break
            else:
                df[target] = 0.0

    print(f'  Physics features applied for {len(countries)} countries.')
    return df

