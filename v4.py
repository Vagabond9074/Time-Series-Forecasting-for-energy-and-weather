def clean_and_optimize(df):
    if 'utc_timestamp' in df.columns:
        df = df[df['utc_timestamp'].dt.year.between(2015, 2020)].copy()
        print(f'  Filtered to {len(df):,} rows (2015–2020)')
    num = df.select_dtypes(include=[np.number]).columns
    df[num] = df[num].ffill().bfill().fillna(0)
    n0 = len(df)
    df = df.drop_duplicates(subset=['utc_timestamp'] if 'utc_timestamp' in df.columns else None)
    if len(df) < n0:
        print(f'  Removed {n0 - len(df)} duplicates')
    for col in df.select_dtypes('float64').columns:
        df[col] = df[col].astype(np.float32)
    return df

def organize_columns_by_country(df_final, countries_with_wind):
    print("\n Organizing columns by country in specified order...")

    wind_cols = [col for col in df_final.columns if col.endswith('_wind') and not col.endswith('_wind_speed')]
    if wind_cols:
        rename_dict = {col: col.replace('_wind', '_wind_power_MW') for col in wind_cols}
        df_final = df_final.rename(columns=rename_dict)
        print(f"   Renamed {len(rename_dict)} wind columns: _wind → _wind_power_MW")

    wind_potential_cols = [col for col in df_final.columns if col.endswith('_wind_potential')]
    if wind_potential_cols:
        df_final = df_final.drop(columns=wind_potential_cols)
        print(f"   Dropped {len(wind_potential_cols)} wind_potential columns")

    ordered_cols = []

    for ts_col in ['utc_timestamp', 'cet_cest_timestamp']:
        if ts_col in df_final.columns:
            ordered_cols.append(ts_col)

    feature_order = [
        '_load', '_temperature', '_solar', '_dew_point',
        '_hdd', '_cdd', '_heating_intensity', '_cooling_intensity', '_wind_power', '_wind_speed_km_h', '_wind_power_MW'

    ]

    for country in sorted(countries_with_wind):
        for feature in feature_order:
            col_name = f"{country}{feature}"
            if col_name in df_final.columns:
                ordered_cols.append(col_name)

    remaining_cols = [col for col in df_final.columns if col not in ordered_cols]
    if remaining_cols:
        ordered_cols.extend(remaining_cols)

    return df_final[ordered_cols]

