def find_max_lag_correlation_scipy(signal1, signal2, max_lag=48):
    """
    Find maximum cross-correlation between two signals within a lag window.
    """
    # Convert to numpy arrays if they are pandas Series
    if hasattr(signal1, 'values'):
        signal1 = signal1.values
    if hasattr(signal2, 'values'):
        signal2 = signal2.values

    if len(signal1) < 20 or len(signal2) < 20:
        return 0.0, 0, None

    # Standardize signals
    s1 = (signal1 - np.mean(signal1)) / (np.std(signal1) + 1e-8)
    s2 = (signal2 - np.mean(signal2)) / (np.std(signal2) + 1e-8)

    # Compute cross-correlation using FFT
    cross_corr = correlate(s1, s2, mode='full', method='fft')
    lags = correlation_lags(len(s1), len(s2), mode='full')

    # Normalize by number of overlapping samples
    lengths = np.minimum(np.arange(1, len(cross_corr) + 1),
                        np.arange(len(cross_corr), 0, -1))
    norm_corr = cross_corr / (lengths + 1e-8)

    # Mask lags within desired range
    mask = np.abs(lags) <= min(max_lag, len(s1) // 4)

    if np.any(mask):
        valid_corr = norm_corr[mask]
        valid_lags = lags[mask]

        # Find maximum absolute correlation
        max_idx = np.argmax(np.abs(valid_corr))
        max_corr = valid_corr[max_idx]
        lag_at_max = valid_lags[max_idx]

        return max_corr, lag_at_max, valid_corr
    else:
        return 0.0, 0, None

def _compute_lagged_correlation(load_signal, feature_signal, max_lag=48):
    """
    Helper function to compute the best cross-correlation between load and a feature,
    considering time lags and detrending.
    Accepts both numpy arrays and pandas Series.
    """
    # Convert to numpy arrays if needed
    if hasattr(load_signal, 'values'):
        load_signal = load_signal.values
    if hasattr(feature_signal, 'values'):
        feature_signal = feature_signal.values

    # Clean data: remove NaN and infinite values
    mask = np.isfinite(load_signal) & np.isfinite(feature_signal)
    v_load = load_signal[mask]
    v_feat = feature_signal[mask]

    if len(v_load) < 20:
        return 0.0

    try:
        # Compute correlation with raw data
        c1, _, _ = find_max_lag_correlation_scipy(v_load, v_feat, max_lag=max_lag)

        # Compute correlation with detrended data (removes long-term trends)
        c2, _, _ = find_max_lag_correlation_scipy(detrend(v_load), detrend(v_feat), max_lag=max_lag)

        # Return the stronger correlation
        return c1 if abs(c1) > abs(c2) else c2
    except Exception as e:
        return 0.0

def compute_row_metrics(df: pd.DataFrame, country_code: str) -> dict:
    """
    Computes correlation metrics between electricity load and various features for a specific country.
    """
    load_col = f'{country_code}_load'

    if load_col not in df.columns:
        return None

    load_series = df[load_col].dropna()
    results = {}

    # 1. Weather Features (Lag-Optimized Cross-Correlation)
    weather_features = {
        'solar': 'Solar',
        'wind_power': 'Wind',
        'temperature': 'Temperature',
        'hdd': 'HDD',
        'cdd': 'CDD',
        'dew_point': 'Dew_Point',
        'wind_speed_km_h': 'Wind_Speed',
        'heating_intensity': 'Heating_Intensity',
        'cooling_intensity': 'Cooling_Intensity'
    }

    for key, display_name in weather_features.items():
        feature_col = f"{country_code}_{key}"
        if feature_col in df.columns:
            feature_series = df[feature_col].dropna()
            common_idx = load_series.index.intersection(feature_series.index)
            if len(common_idx) > 20:
                results[display_name] = _compute_lagged_correlation(
                    load_series.loc[common_idx],
                    feature_series.loc[common_idx]
                )
            else:
                results[display_name] = 0.0
        else:
            results[display_name] = 0.0

    # 2. Lag & Rolling Features (Standard Pearson Correlation)
    lag_roll_mapping = [
        (f'{country_code}_load_lag_1h', 'Load_Lag_1h'),
        (f'{country_code}_load_lag_24h', 'Load_Lag_24h'),
        (f'{country_code}_load_lag_168h', 'Load_Lag_168h'),
        (f'{country_code}_load_roll_24h', 'Load_Roll_24h'),
        (f'{country_code}_load_roll_7d', 'Load_Roll_7d')
    ]

    for col_name, display_name in lag_roll_mapping:
        if col_name in df.columns:
            corr_val = load_series.corr(df[col_name])
            results[display_name] = round(corr_val, 4) if pd.notna(corr_val) else 0.0
        else:
            results[display_name] = 0.0

    # 3. Temporal Features (Standard Pearson Correlation)
    temporal_features = {
        'hour': 'Hour',
        'day_of_week': 'Day_of_Week',
        'is_weekend': 'Is_Weekend',
        'month': 'Month',
        'is_holiday': 'Is_Holiday'
    }

    for col_name, display_name in temporal_features.items():
        if col_name in df.columns:
            corr_val = load_series.corr(df[col_name])
            results[display_name] = round(corr_val, 4) if pd.notna(corr_val) else 0.0
        else:
            results[display_name] = 0.0

    # 4. Season (Categorical to Numeric)
    if 'season' in df.columns:
        season_map = {'winter': 1, 'spring': 2, 'summer': 3, 'autumn': 4}
        season_numeric = df['season'].map(season_map)
        corr_val = load_series.corr(season_numeric)
        results['Season'] = round(corr_val, 4) if pd.notna(corr_val) else 0.0
    else:
        results['Season'] = 0.0

    return results

def compute_cross_correlation_matrix(df: pd.DataFrame, countries: list) -> pd.DataFrame:
    """
    Computes a cross-correlation matrix for all countries in the dataset.
    """
    print(f"Computing correlations for {len(countries)} countries...")

    # Compute correlations for each country
