def build_hourly_dataset(df_final, countries):
    df = df_final.copy()
    if 'utc_timestamp' in df.columns:
        df.index = pd.to_datetime(df['utc_timestamp'])
        df = df.drop(columns=['utc_timestamp','cet_cest_timestamp'], errors='ignore')
    df = df.select_dtypes(include=[np.number]).resample('h').mean().ffill().bfill()

    df['hour_sin']  = np.sin(2*np.pi*df.index.hour      / 24)
    df['hour_cos']  = np.cos(2*np.pi*df.index.hour      / 24)
    df['dow_sin']   = np.sin(2*np.pi*df.index.dayofweek / 7)
    df['dow_cos']   = np.cos(2*np.pi*df.index.dayofweek / 7)
    df['month_sin'] = np.sin(2*np.pi*df.index.month     / 12)
    df['month_cos'] = np.cos(2*np.pi*df.index.month     / 12)

    for country in countries:
        col = f'{country}_load'
        if col not in df.columns:
            continue
        s = df[col]
        df[f'{country}_load_lag_1h']   = s.shift(1)
        df[f'{country}_load_lag_24h']  = s.shift(24)
        df[f'{country}_load_lag_168h'] = s.shift(168)
        df[f'{country}_load_roll_24h'] = s.rolling(24,  min_periods=1).mean()
        df[f'{country}_load_roll_7d']  = s.rolling(168, min_periods=1).mean()

    df['hour']        = df.index.hour
    df['day_of_week'] = df.index.dayofweek
    df['is_weekend']  = (df.index.dayofweek >= 5).astype(np.int8)
    df['month']       = df.index.month
    df['season']      = df['month'].map({
        12:'winter',1:'winter',2:'winter',
        3:'spring', 4:'spring',5:'spring',
        6:'summer', 7:'summer',8:'summer',
        9:'autumn',10:'autumn',11:'autumn',
    })
    df = df.fillna(0)

    print(f'Hourly dataset shape: {df.shape}')
    return df

